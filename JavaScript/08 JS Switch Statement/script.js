// Erstelle eine Zufallszahl zwischen 5 und 10

let zahl = Math.floor(Math.random() * 6) + 5;

switch (zahl) {
    case 5:
        console.log("Five");
        break;
    case 6:
        console.log("Six");
        break;
    case 7:
        console.log("Seven");
        break;
    case 8:
        console.log("Eight");
        break;
    case 9:
        console.log("Nine");
        break;
    case 10:
        console.log("Ten");
        break;
}

    